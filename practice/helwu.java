package practice;


//output of relational operator is always a boolean

public class helwu {
    public static void main(String[] args) {
        int a =10, b=20;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==10 && b<100);
    }
}
